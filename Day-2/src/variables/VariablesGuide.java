package variables;

public class VariablesGuide {
    public static void main(String[] args){

        /**
         * Variables
         *
         * byte - 8-bit signed two's complement integer
         * short - 16-bit signed two's complement integer
         * int - 32-bit signed two's complement integer
         * long - 64-bit signed two's complement integer
         * float - 32-bit IEEE 754 floating point
         * double - 64-bit IEEE 754 floating point
         * char - 16-bit Unicode character
         * boolean - true or false
         *
         */


        /**
         * All Integers without decimal
         */
        byte byteVariable = 127;
        short shortVariable = 32767;
        int intVariable = 2147483647;
        long longVariable = 9223372036854775807L;

        /**
         * All Floats with decimal
         */
        float floatVariable = 3.4028235E38f;
        double doubleVariable = 1.7976931348623157E308;

        /**
         * Character of Alphabet
         */
        char charVariable = 'A';
        /**
         * Boolean
         */
        boolean booleanVariable = true;


        /**
         * Rules
         * 1. Variable names are case-sensitive
         * 2. Variable names must start with a letter, $, or _
         * 3. Variable names can contain letters, numbers, $, or _
         * 4. Variable names cannot contain spaces
         * 5. Variable names cannot be a reserved keyword
         * 6. Variable names should be in camelCase
         * 8. Variable names should not start with a number
         */



    }
}
